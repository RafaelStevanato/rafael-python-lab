# underscore in numbers
number1 = 12000760
number2 = 12_000_760
# this numbers are considered the same number.
print(number1)
print(number2)

# schientific notation
#3e4 = 3E4 = 3 * 10000 = 30000
#3e-4 = 3E-4 = 3 * 1/10000 = 0.0003
print(3e4)
print(3E4)

# octal numbers
# start with 0O or 0o
print(0o123)

#hexadecimal number
print(0x123)