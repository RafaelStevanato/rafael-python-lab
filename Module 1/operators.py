# module division (Let's say that you work in a car company, and you produce wheels for cars. Your boss comes and says: "We need to quickly assembly as many cars as we can. We need the wheels that you produce in sets of 4. How many cars can we produce with the wheels that you have at the moment?"
#You know that 1 car needs 4 wheels. Now, if you have 5 wheels in total in your factory, you will be able to create only 1 set of 4 wheels for 1 car, and you will then have 1 wheel left (5 = 4 * 1 + 1). This "1 wheel left" is the remainder. And it's also the result of modulo division. 5 % 4 = 1.
#If you have 7 wheels, you can still only create 1 set of 4 wheels, and you will be left with 3 wheels (7 = 4 * 1 + 3). The "3 wheels left" is the remainder. 7 % 4 = 3.
#If you have 10 wheels, you can create 2 sets of 4 wheels, and you will be left with 2 wheels (10 = 4 * 2 + 2). The "2 wheels left" is the remainder. 10 % 4 = 2.
#If you only have 3 wheels in total, how many cars can you assemble? Zero. 3 wheels is not enough even for a single car, so you can produce 0 sets, and you will be left with 3 wheels. 3 % 4 = 3.)
7%2

