#How to insert an ' inside of string?
#With double quotes around the string it works
print ("I'm studying")

#And with one ' we can use \ inside the string to how
print ('I\'m studying')

#If we do this:
print("I'm Studying")
print("and having fun!")
#It'll show in two lines in the output

#To them to appear in the same line, we can do this:
print("I'm studying", "and having fun!")

#To add a new line inside the string, we can use \n:
print("I'm studying", "\nand having fun!")

#We can make an expression using +:
print ("This is " + "an expression")